// js_032_Math.js

// 최대값
console.log(Math.max(1, 2, 3, 4, 5)); //5

// 최소값
console.log(Math.min(1, 2, 3, 4, 5)); //1

// 절대값
console.log(Math.abs(-10)); //10

// 반올림 - 자릿수 지정할 수 있음
console.log(Math.round(25.86355)); //26

// 올림
console.log(Math.round(23.1255)); //23

// 내(버)림
console.log(Math.round(23.56)); //23

// 난수 0.0 이상 ~ 1.0 미만
console.log(Math.random());

// 반올림
console.log((25.523).toFixed(2)); //25.52
console.log((25.526).toFixed(2)); //25.53 - 반올림이 이루어짐
